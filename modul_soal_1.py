def penjumlahan(a,b):
    penjumlahan = a + b
    print(f'Hasil Penjumlahan anda Adalah: {penjumlahan}')


def pengurangan(a, b):
    pengurangan = a - b 
    print(f'Hasil Pengurangan anda Adalah: {pengurangan}')

def perkalian(a, b):
    perkalian = a * b 
    print(f'Hasil Perkalian anda Adalah: {perkalian}')
